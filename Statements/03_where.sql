/*
Selecciona todos los elementos de la tabla users donde solo la edad sea igual a 15.
*/
SELECT * FROM users WHERE age = 15;

/*
Selecciona los nombres distintos de la tabla users, donde su edad sea igual a 15.
*/
SELECT DISTINCT name FROM users WHERE age = 15;

